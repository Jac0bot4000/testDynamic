        <h2 id="whaleWatching">Whale Watching</h2>
            <p>Whether in Provincetown for the day, or the Mid-Cape for a week, there is no shortage of whale watches that depart from port. Spend a few hours out on the ocean with your family observing some of the largest mammals in the world. Off the coast of the cape, there is a chance to see humpback, finback, and minke whales <a href="references.html#26">(26)</a>. During the tour, your guides will not only point out the wildlife, but oftentimes share stories about them since the whales have been in the area for many years.</p>

        <h2>Provincetown</h2>
            <p class="address">Commercial St, Provincetown, MA 02657</p>
            <p>By far the largest collection of shops and restaurants on the Cape is that of Commercial St in Provincetown, Massachusetts. Here, you can find over 100 shops of all varieties, including local boutiques and art galleries, to candy and toy stores. There are too many shops to possibly visit in one day, so make sure to plan accordingly. Additionally, there is a plethora of restaurants and bars in the area, and there is a thriving night life surrounding P-Town. Historically speaking, Provincetown has always been a LGBTQ safe space, meaning much of the area is very heavily pride-themed and all are welcome. Make sure to take in all there is to offer, and enjoy your time in the area that is alike none other.</p>
            <p>After you’ve had time to peruse Commercial Street, be sure to check out the pier and all of its offerings, or the historic Pilgrim monument that can be seen from across the bay from as far as Yarmouth and Dennis.</p>

        <h2 id="hyannis">Hyannis</h2>
            <p class="address">367 Main St, Hyannis, MA 02601</p>
            <p>Quite possibly one of the largest and most extensive shopping areas in Mid-Cape is Main St, Hyannis. This long stretch of shops features all sorts of shops, from small boutiques, antiques, and clothing, to toys, pets, and collectible stores; your entire family is sure to find a shop that suits their fancy. After exploring everything there is to see, there are many options to feast from for a great dinner, from casual grills to more upscale restaurants, there is certainly something to enjoy for dinner. </p>
            <p>If you move a bit closer to the water, there is also a small plaza that has stands with rotating weekly vendors of local artists displaying their work for sale. Since they rotate, expect to find all variety of choices to shop from each week. After exploring what the vendors have to offer, take a leisurely stroll along the pier and watch as ships come in and out of the harbor.</p>
            <p>In the event of poor weather, Hyannis also sports a mall to shop through that has more known national brands, but allows for shopping while protected from the elements.</p>
